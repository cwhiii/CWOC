package com.cwoc.app.data.remote.dto

/**
 * DTO matching the server's chit JSON response shape.
 * Fields use snake_case to match server field names (Gson handles deserialization).
 * Complex nested JSON objects use Any? since we store them as raw JSON strings in Room.
 */
data class ChitDto(
    val id: String,
    val title: String?,
    val note: String?,
    val tags: List<String>?,
    val start_datetime: String?,
    val end_datetime: String?,
    val due_datetime: String?,
    val point_in_time: String?,
    val completed_datetime: String?,
    val status: String?,
    val priority: String?,
    val severity: String?,
    val checklist: Any?,
    val alarm: Boolean?,
    val notification: Boolean?,
    val recurrence: String?,
    val recurrence_id: String?,
    val recurrence_rule: Any?,
    val recurrence_exceptions: Any?,
    val location: String?,
    val color: String?,
    val people: List<String>?,
    val pinned: Boolean?,
    val archived: Boolean?,
    val deleted: Boolean?,
    val created_datetime: String?,
    val modified_datetime: String?,
    val is_project_master: Boolean?,
    val child_chits: List<String>?,
    val all_day: Boolean?,
    val timezone: String?,
    val alerts: Any?,
    val progress_percent: Int?,
    val time_estimate: String?,
    val weather_data: Any?,
    val health_data: Any?,
    val habit: Boolean?,
    val habit_goal: Int?,
    val habit_success: Int?,
    val show_on_calendar: Boolean?,
    val habit_reset_period: String?,
    val habit_last_action_date: String?,
    val habit_hide_overall: Boolean?,
    val perpetual: Boolean?,
    val shares: Any?,
    val stealth: Boolean?,
    val assigned_to: String?,
    val owner_id: String?,
    val owner_display_name: String?,
    val owner_username: String?,
    val has_unviewed_conflict: Boolean?,
    val availability: String?,
    val snoozed_until: String?,
    val prerequisites: List<String>?,
    val sync_version: Int = 0,

    // Email fields
    val email_message_id: String? = null,
    val email_from: String? = null,
    val email_to: Any? = null,
    val email_cc: Any? = null,
    val email_bcc: Any? = null,
    val email_subject: String? = null,
    val email_body_text: String? = null,
    val email_date: String? = null,
    val email_folder: String? = null,
    val email_status: String? = null,
    val email_read: Boolean? = null,
    val email_in_reply_to: String? = null,
    val email_references: String? = null,
    val email_body_html: String? = null,
    val email_account_id: String? = null,
    val email_send_at: String? = null,
    val email_request_read_receipt: Any? = null,

    // Attachments
    val attachments: Any? = null,

    // Checklist/thread fields
    val checklist_autosave: Boolean? = null,
    val nest_thread_id: String? = null,
    val auto_complete_checklist: Boolean? = null
)
